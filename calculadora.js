class Calculadora{
    sumar(num1, num2){
        let suma = num1 + num2;
        return suma
    }
    restar(num1, num2){
        let resta = num1 - num2;
        return resta
    }
    dividir(num1, num2){
        let division = num1 / num2;
        return division
    }
    multiplicar(num1, num2){
        let producto = num1 * num2;
        return producto
    }
}